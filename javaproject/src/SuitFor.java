public class SuitFor {
    public static void main(String[] args) {

                // Affichage en ordre croissant (0 à 10)
                System.out.print("Suite ascendante : ");
                for (int i = 0; i <= 10; i++) {
                    System.out.print(i + " ");
                }

                System.out.println(); // Nouvelle ligne

                // Affichage en ordre décroissant (10 à 0)
                System.out.print("Suite descendante : ");
                for (int i = 10; i >= 0; i--) {
                    System.out.print(i + " ");
                }
            }
        }
