public class WhileBoucle {
    public static void main(String[] args) {
        int i = 2; // Commence à 2 (premier nombre pair)

        while (i <= 20) { // Tant que i est ≤ 20
            System.out.println(i); // Afficher i
            i += 2; // Passer au prochain nombre pair
        }
    }
}
